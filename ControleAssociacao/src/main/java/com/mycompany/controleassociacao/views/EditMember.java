    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        MemberController memberController = new MemberController();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        String name = nameField.getText();
        String birthDate = birthDateField.getText();
        String address = addressField.getText();
        String rg = RGField.getText();
        String cellphone = cellphoneField.getText();

        if (Validate.hasEmptyData(new ArrayList<>(Arrays.asList(name, birthDate, address, rg, cellphone)))) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar.",
                    "Dados vazios", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LocalDate birthLocalDate;
        try {
            birthLocalDate = LocalDate.parse(birthDate, formatter);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Insira a data de nascimento no formato dd/MM/yyyy",
                    "Data inv√°lida", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Member updatedMember = new Member(name, address, birthLocalDate, cellphone, rg);

        String response = memberController.updateMember(id, updatedMember);
        JOptionPane.showMessageDialog(null, response,
                "Opera√ß√£o finalizada", JOptionPane.INFORMATION_MESSAGE);

        this.dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

    private void loadMemberData() {
        MemberController memberController = new MemberController();
        Member member = memberController.getMemberById(this.id);
        if (member != null) {
            nameField.setText(member.getName());
            RGField.setText(member.getRg());
            birthDateField.setText(member.getBirthDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
            addressField.setText(member.getAddress());
            cellphoneField.setText(member.getCellphone());
            
            title.setText("Editar " + member.getName());
        } else {
            JOptionPane.showMessageDialog(this, "Membro n√£o encontrado!", "Error", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }

